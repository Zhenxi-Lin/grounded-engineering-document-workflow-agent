logging.basicConfig(
    level=logging.INFO,
    format="%(levelname)s %(name)s: %(message)s",
)
LOGGER = logging.getLogger("build_crawl_plan")


@dataclass(frozen=True)
class PageCategory:
    category_name: str
    priority: str
    include_patterns: list[str]
    notes: str


@dataclass(frozen=True)
class ExcludeRule:
    rule_type: str
    pattern: str
    reason: str


@dataclass(frozen=True)
class SourcePlan:
    source_name: str
    base_url: str
    priority_level: str
    page_categories: list[PageCategory]
    example_paths: list[str]
    exclude_rules: list[ExcludeRule]
    crawl_notes: list[str]


