def global_exclude_rules() -> list[dict[str, str]]:
    rules = [
        ExcludeRule("substring", "/search", "Skip search and navigation endpoints."),
        ExcludeRule("substring", "/genindex", "Skip autogenerated indexes."),
        ExcludeRule("substring", "/_downloads/", "Skip PDFs and binary assets."),
        ExcludeRule("substring", "/_sources/", "Skip source artifacts."),
        ExcludeRule("substring", "release_notes", "Skip release archives and changelogs."),
        ExcludeRule("substring", "how_to_contribute", "Skip authoring and contributor pages."),
    ]
    return [asdict(rule) for rule in rules]


def build_crawl_plan() -> dict[str, Any]:
    sources = [
        build_ros2_plan(),
        build_px4_plan(),
        build_moveit_plan(),
        build_isaac_ros_plan(),
    ]

    priority_order = {"P0": 0, "P1": 1, "P2": 2}
    sources.sort(key=lambda plan: (priority_order.get(plan.priority_level, 99), plan.source_name))

    return {
        "plan_name": "high_value_engineering_docs_mvp",
        "project_name": "Grounded Engineering Document Workflow Agent",
        "generated_at_utc": datetime.now(timezone.utc).isoformat().replace("+00:00", "Z"),
        "selection_objective": [
            "evidence-backed Q&A",
            "document and procedure comparison",
            "checklist extraction",
        ],
        "global_include_focus": [
            "installation",
            "setup",
            "tutorials",
            "concepts",
            "troubleshooting",
            "integration",
            "safety",
        ],
        "global_exclude_rules": global_exclude_rules(),
        "sources": [asdict(source) for source in sources],
    }


def write_crawl_plan(output_path: Path) -> Path:
    plan = build_crawl_plan()
    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(
        json.dumps(plan, indent=2, ensure_ascii=False) + "\n",
        encoding="utf-8",
    )
    LOGGER.info("Wrote crawl plan to %s", output_path)
    return output_path


def main() -> None:
    project_root = Path(__file__).resolve().parents[1]
    output_path = project_root / "data" / "crawl_plan.json"
    write_crawl_plan(output_path)


if __name__ == "__main__":
    main()
